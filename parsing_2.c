#include "ft_printf.h"

int			is_flag(char c)
{
	return (c == '-' || c == '.' || c == '0' || c == '*');
}

int			is_type(char c)
{
	return (c == 'c' || c == 's' || c == 'p' || c == 'd' ||
			c == 'i' || c == 'u' || c == 'x' || c == 'X' || c == '%');
}
